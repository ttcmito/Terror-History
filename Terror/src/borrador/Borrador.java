package borrador;

public class Borrador {

	public static void main(String[] args) {
		
		System.out.println("—Tu padre fue mi asesino hace muchos años —dijo Shiro, con una voz que resonaba como un eco helado\n"
				+ "en la noche—. Lo supe en el instante en que vi ese anillo en su mano, el anillo con una pirámide grabada.\n"
				+ "Esa imagen fue lo último que mis ojos vieron antes de morir.");
		System.out.println();
		System.out.println("José sollozó, incapaz de contener la verdad.");
		System.out.println();
		System.out.println("—Shiro... Era un hombre desesperado. Quería vivir en esa mansión, y tú eras un obstáculo. Pero nunca\n"
				+ "imaginé que mi pecado me alcanzaría aquí, en esta casa, tantos años después.");
		System.out.println();
		System.out.println("Shiro avanzó lentamente hacia Pedro.");
		System.out.println();
		System.out.println("—Lo siento —murmuró José, lágrimas rodando por sus mejillas—. Pero, por favor, no le hagas daño a mi hijo.\n"
				+ "Es inocente.");
		System.out.println();
		System.out.println("—¿Inocente? —replicó Shiro, con una furia creciente—. Mi vida fue arrebatada. Mi padre murió de tristeza.\n"
				+ "Yo perdí todo. Ahora, tú perderás lo mismo.");
		System.out.println();
		System.out.println("Shiro extendió sus manos hacia Pedro, que estaba paralizado por el miedo. La linterna cayó al suelo, rodando\n"
				+ "y dejando a ambos en la penumbra. José, en un acto desesperado, se abalanzó hacia Shiro.");
		System.out.println();
		System.out.println("—¡Llévame a mí! ¡Déjalo a él!");
		System.out.println();
		System.out.println("Pero la decisión de Shiro ya estaba tomada. Un grito desgarrador resonó en la noche cuando Pedro cayó al suelo, inerte.");
		System.out.println();
		System.out.println("José abrazó el cuerpo de su hijo, con un dolor que parecía desgarrarle el alma. Shiro, al ver su sufrimiento, se desvaneció,\n"
				+ "satisfecha con su venganza. La mansión quedó nuevamente en silencio.");
		System.out.println();
		System.out.println("Poco después, José volvió al puente. El anillo que había sellado su destino brillaba en su mano. Con una última mirada al río\n"
				+ "que corría bajo sus pies, dejó caer el anillo y saltó, poniendo fin a su vida.");
		System.out.println();
		System.out.println("La vieja mansión quedó vacía una vez más, atrapada entre los ecos de un pasado oscuro y las sombras de los que jamás encontraron paz.");
	}

}
